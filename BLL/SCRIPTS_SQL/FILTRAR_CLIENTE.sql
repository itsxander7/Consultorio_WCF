CREATE PROCEDURE [SCH_ADMIN].[SP_FILTAR_CLIENTES]
(
@ID NVARCHAR(50)
)
AS

BEGIN

SELECT [ID_CLIENTE]
      ,[NOMBRE_CLIENTE]
      ,[APELLIDO1_CLIENTE]
      ,[APELLIDO2_CLIENTE]
      ,[DIRECCION_CLIENTE]
      ,[TELEFONO_CLIENTE]
      ,[CORREO_CLIENTE]
  FROM [SCH_ADMIN].[CLIENTES]
  WHERE [ID_CLIENTE]=@ID

END

GO